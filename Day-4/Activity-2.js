// Task 3: Program to calculate the sum of numbers from 1 to 10 using a while loop 

let i = 0;
let sum = 0;

while(i<=10){
    sum = sum + i;
    i++;
}

console.log("Sum of 1 to 10 =", sum);

// Task 4: Program to print numbers from 10 to 1 using while loop

let n = 10;

while (n >= 1){
    console.log(n);
    n--;
}

