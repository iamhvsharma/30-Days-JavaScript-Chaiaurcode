// Task 10: Program to compare two numbers using == and === and log the results

// == 
let p = 10; // Number type variable
let q = "10"; // String type variable

// Let'check equality using double equals
console.log(p == q); 

// Gives True as == only checks the value not the datatype hence it gives true but the p and q are not equal as p is number and q is string

// Let's check equality using triple equals
console.log(p === q);

// Gives False as === checks the value and datatype both to decide wheater two variable are equal or not